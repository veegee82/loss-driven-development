class TestBiasInvariance:
    def test_calibrator_log_does_not_mutate_metric_spec(
        self, tmp_path: Path
    ) -> None:
        """Logging calibration pairs must NOT change the metric's spec
        or the observed() output for any input."""
        store = TraceStore(tmp_path)
        reg = MetricRegistry(store)
        m = bounded_rate("m", lambda o: (o["x"], o["y"]))
        reg.register(m)
        cal = Calibrator(reg)

        # Observe a baseline
        original = m.observed({"x": 3, "y": 10})

        # Log many calibration pairs that would appear to "reward" high values
        for _ in range(20):
            cal.log("m", predicted=0.0, observed=1.0)

        # Observe must be unchanged
        assert m.observed({"x": 3, "y": 10}) == original
        # Spec must be unchanged
        assert reg.specs()["m"].version == m.spec.version
        assert reg.specs()["m"].advisory_only is True

    def test_promotion_does_not_change_observed(self, tmp_path: Path) -> None:
        """Even after a metric is promoted to load_bearing, observed()
        returns the same value for the same input."""
        store = TraceStore(tmp_path)
        reg = MetricRegistry(store)
        m = bounded_rate("m", lambda o: (o["x"], o["y"]))
        reg.register(m)
        cal = Calibrator(reg)

        original = m.observed({"x": 3, "y": 10})

        # Force a promotion by logging perfect calibration
        for _ in range(6):
            cal.log("m", 0.3, 0.3)
        assert cal.try_promote("m") is True

        # Observed is still the same
        assert m.observed({"x": 3, "y": 10}) == original

    def test_metric_spec_is_frozen(self) -> None:
        """Specs cannot be mutated after construction (frozen dataclass)."""
        s = MetricSpec(name="x", kind="bounded", unit="rate")
        with pytest.raises(Exception):
            s.version = 99  # type: ignore


