# Hypothesis strategies
floats_bounded = st.floats(min_value=0.0, max_value=1.0, allow_nan=False, allow_infinity=False)
floats_positive = st.floats(min_value=0.0, max_value=1e6, allow_nan=False, allow_infinity=False)
floats_signed = st.floats(
    min_value=-100.0, max_value=100.0, allow_nan=False, allow_infinity=False
)
weights_strategy = st.floats(
    min_value=0.001, max_value=100.0, allow_nan=False, allow_infinity=False
)


