class TestMaxMinProperties:
    @given(floats_bounded)
    def test_max_idempotent(self, v: float) -> None:
        m = bounded_rate("x", lambda o, x=v: (x, 1))
        mx = maximum("x_max", [m, m])
        assert mx.observed(None) == pytest.approx(v)

    @given(floats_bounded)
    def test_min_idempotent(self, v: float) -> None:
        m = bounded_rate("x", lambda o, x=v: (x, 1))
        mn = minimum("x_min", [m, m])
        assert mn.observed(None) == pytest.approx(v)

    @given(floats_bounded, floats_bounded)
    def test_max_commutative(self, v1: float, v2: float) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        mx_ab = maximum("ab", [m1, m2])
        mx_ba = maximum("ba", [m2, m1])
        assert mx_ab.observed(None) == pytest.approx(mx_ba.observed(None))

    @given(floats_bounded, floats_bounded)
    def test_min_commutative(self, v1: float, v2: float) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        mn_ab = minimum("ab", [m1, m2])
        mn_ba = minimum("ba", [m2, m1])
        assert mn_ab.observed(None) == pytest.approx(mn_ba.observed(None))

    @given(floats_bounded, floats_bounded)
    def test_min_le_max(self, v1: float, v2: float) -> None:
        """min(L1, L2) ≤ max(L1, L2) for any values."""
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        mn = minimum("x_min", [m1, m2])
        mx = maximum("x_max", [m1, m2])
        assert mn.observed(None) <= mx.observed(None) + 1e-9


