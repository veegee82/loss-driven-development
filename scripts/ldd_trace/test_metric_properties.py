class TestWeightedSumProperties:
    @given(floats_bounded, floats_bounded, weights_strategy, weights_strategy)
    def test_output_is_bounded_01(
        self, v1: float, v2: float, w1: float, w2: float
    ) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        ws = weighted_sum("ab", [(m1, w1), (m2, w2)])
        out = ws.observed(None)
        assert 0.0 <= out <= 1.0

    @given(floats_bounded, floats_bounded, weights_strategy, weights_strategy)
    def test_commutative_same_weights(
        self, v1: float, v2: float, w1: float, w2: float
    ) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        ws_ab = weighted_sum("ab", [(m1, w1), (m2, w2)])
        ws_ba = weighted_sum("ba", [(m2, w2), (m1, w1)])
        assert ws_ab.observed(None) == pytest.approx(ws_ba.observed(None))

    @given(
        floats_bounded, floats_bounded,
        weights_strategy, weights_strategy,
        st.floats(min_value=0.1, max_value=100.0, allow_nan=False, allow_infinity=False),
    )
    def test_homogeneous_in_weights(
        self, v1: float, v2: float, w1: float, w2: float, lam: float
    ) -> None:
        """Scaling all weights by λ does not change the output."""
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        ws = weighted_sum("ab", [(m1, w1), (m2, w2)])
        ws_scaled = weighted_sum("ab2", [(m1, w1 * lam), (m2, w2 * lam)])
        assert ws.observed(None) == pytest.approx(ws_scaled.observed(None))

    @given(floats_bounded, floats_bounded)
    def test_equal_weights_is_mean(self, v1: float, v2: float) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        m2 = bounded_rate("b", lambda o, x=v2: (x, 1))
        ws = weighted_sum("ab", [(m1, 1.0), (m2, 1.0)])
        assert ws.observed(None) == pytest.approx((v1 + v2) / 2)

    @given(floats_bounded, floats_bounded)
    def test_single_component_weighted_sum_is_identity(
        self, v1: float, _unused: float
    ) -> None:
        m1 = bounded_rate("a", lambda o, x=v1: (x, 1))
        ws = weighted_sum("a1", [(m1, 1.0)])
        assert ws.observed(None) == pytest.approx(v1)


