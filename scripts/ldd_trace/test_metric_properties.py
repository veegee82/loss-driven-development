class TestDistributionalProperties:
    @given(st.lists(floats_bounded, min_size=2, max_size=10))
    def test_max_equals_numpy_like_max_of_list(self, values: List[float]) -> None:
        """Our composite Max on N values agrees with builtin max."""
        metrics = [
            bounded_rate(f"m{i}", lambda o, x=v: (x, 1))
            for i, v in enumerate(values)
        ]
        mx = maximum("m_all", metrics)
        assert mx.observed(None) == pytest.approx(max(values))

    @given(st.lists(floats_bounded, min_size=2, max_size=10))
    def test_min_equals_numpy_like_min_of_list(self, values: List[float]) -> None:
        metrics = [
            bounded_rate(f"m{i}", lambda o, x=v: (x, 1))
            for i, v in enumerate(values)
        ]
        mn = minimum("m_all", metrics)
        assert mn.observed(None) == pytest.approx(min(values))


