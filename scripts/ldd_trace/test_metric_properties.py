class TestGamingGuard:
    @given(st.sampled_from(list(GAMING_GUARD_PHRASES)))
    def test_any_self_ref_phrase_rejected(self, phrase: str) -> None:
        with pytest.raises(ValueError, match="gaming-guard"):
            MetricSpec(
                name="x",
                kind="bounded",
                unit="rate",
                description=f"this metric tracks {phrase} quality",
            )

    def test_neutral_descriptions_pass(self) -> None:
        """Descriptions about WHAT is measured (not about current action) pass."""
        MetricSpec(
            name="x", kind="bounded", unit="rate",
            description="fraction of failing assertions in the test suite",
        )
        MetricSpec(
            name="y", kind="positive", unit="ms",
            description="wall-clock latency from request to response",
        )
        MetricSpec(
            name="z", kind="signed", unit="dimensionless",
            description="delta between observed and predicted count",
        )


