"""Property-based tests for the Metric Algebra — v0.9.0.

These tests go beyond fixed unit assertions: they establish ALGEBRAIC LAWS
over the composition operators + the normalization contracts. If an
implementation violates one of these properties for ANY input generated by
hypothesis, the test fails.

Laws proven here (each becomes a test):

  NORMALIZE
    1. Idempotency:     normalize(normalize(v)) == normalize(v)
    2. Bounded range:   normalize(v) ∈ [0, 1]  ∀v ∈ ℝ
    3. Monotonic (for bounded and positive, in valid domain)

  WEIGHTED SUM
    4. Bounded output:   ws ∈ [0, 1]
    5. Commutative in components (same weights)
    6. Linear in weights scaling (homogeneity):
       ws([m1, m2], [λw1, λw2])(x) == ws([m1, m2], [w1, w2])(x)
    7. Equal-weight weighted sum = arithmetic mean of normalized components
    8. Weight-0 component has no effect (degenerates to remaining sum)

  MAX / MIN
    9. Idempotency:  max(L, L) == L   and   min(L, L) == L
    10. Commutativity: max(L1, L2) == max(L2, L1)
    11. Duality relation:  max(L1, L2) + min(1-L1, 1-L2) ≡ 1  NOT strictly (uses inverted metrics)
        Simpler duality: max(normalized) ≥ min(normalized)  always
    12. max(single) == normalize(observed(single))

  BIAS-INVARIANCE (load-bearing)
    13. Memory-state-independence: calibrator.log() doesn't change metric.observed() output
        for any θ across all metrics registered.
    14. Spec-immutability: any mutation attempt on a registered spec raises

  GAMING-GUARD
    15. Any spec with self-referential description is rejected at construction
"""
