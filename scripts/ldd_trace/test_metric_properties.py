class TestNormalizeProperties:
    @given(floats_bounded)
    def test_bounded_normalize_idempotent(self, v: float) -> None:
        m = bounded_rate("x", lambda o: (0, 1))
        once = m.normalize(v)
        twice = m.normalize(once)
        assert once == pytest.approx(twice)

    @given(st.floats(min_value=-1000.0, max_value=1000.0, allow_nan=False, allow_infinity=False))
    def test_bounded_normalize_stays_in_01(self, v: float) -> None:
        m = bounded_rate("x", lambda o: (0, 1))
        n = m.normalize(v)
        assert 0.0 <= n <= 1.0

    # NOTE: PositiveCount.normalize is NOT idempotent by design — its input
    # domain (raw [0,∞)) differs from its output range ([0,1]). Idempotency
    # is an algebraic law only of BoundedRate. For PositiveCount and
    # SignedDelta, we instead assert the stronger "output bounded in [0,1]"
    # and "monotonic non-decreasing within the non-saturated domain".

    @given(floats_positive)
    def test_positive_normalize_bounded(self, v: float) -> None:
        m = positive_count("x", lambda o: o, normalize_scale=100.0)
        n = m.normalize(v)
        assert 0.0 <= n <= 1.0

    @given(floats_signed)
    def test_signed_normalize_bounded(self, v: float) -> None:
        m = signed_delta("x", lambda o: o, normalize_scale=1.0)
        n = m.normalize(v)
        assert 0.0 <= n <= 1.0

    @given(floats_signed)
    def test_signed_normalize_zero_is_half(self, v: float) -> None:
        """normalize(0) must equal 0.5 (centered sigmoid)."""
        m = signed_delta("x", lambda o: o)
        assert m.normalize(0.0) == pytest.approx(0.5)

    @given(
        st.floats(min_value=-10.0, max_value=10.0, allow_nan=False, allow_infinity=False),
        st.floats(min_value=-10.0, max_value=10.0, allow_nan=False, allow_infinity=False),
    )
    def test_signed_normalize_monotonic_non_decreasing(
        self, a: float, b: float
    ) -> None:
        """For a ≤ b, normalize(a) ≤ normalize(b). (Non-decreasing is the
        strict guarantee — strict-less-than can fail at sigmoid saturation
        for |v| > ~30 due to float64 precision.)"""
        assume(a <= b)
        m = signed_delta("x", lambda o: o, normalize_scale=1.0)
        assert m.normalize(a) <= m.normalize(b) + 1e-12


